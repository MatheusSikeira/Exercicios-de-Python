num1 = int(input('Digite o primeiro número: '))

num2 = int(input('Digite o segundo número: '))

ResultadoSoma = num1 + num2

ResultadoSubtração = num1 - num2

ResultadoMultiplicação = num1 * num2

ResultadoDivisão = num1 / num2

print(f"Resultado da soma: {ResultadoSoma}")
print(f"Resultado da subtração: {ResultadoSubtração}")
print(f"Resultado da multiplicação: {ResultadoMultiplicação}")
print(f"Resultado da divisão: {ResultadoDivisão}")